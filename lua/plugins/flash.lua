return {
  "folke/flash.nvim",
  event = "VeryLazy",
  ---@type Flash.Config
  opts = {
    search = {
      mode = "search",
    },
  },
  keys = {
    { "S", mode = { "n", "o", "x" }, false },

    {
      "s",
      mode = { "n", "x", "o" },
      function()
        require("flash").jump()
      end,
      desc = "Flash continue",
    },

    {
      "<M-w>",
      mode = { "n", "x", "o" },
      function()
        require("flash").jump({
          pattern = vim.fn.expand("<cword>"),
        })
      end,
      desc = "Flash current word",
    },

    {
      "<M-l>",
      mode = { "n", "x", "o" },
      function()
        require("flash").jump({
          search = { mode = "search", max_length = 0 },
          label = { after = { 0, 0 } },
          pattern = "^",
        })
      end,
      desc = "Flash line start",
    },

    {
      "<M-s>",
      mode = { "n", "x", "o" },
      function()
        require("flash").jump({
          pattern = ".",
          search = {
            mode = function(pattern)
              if pattern:sub(1, 1) == "." then
                pattern = pattern:sub(2)
              end
              return ([[\<%s\w*\>]]):format(pattern), ([[\<%s]]):format(pattern)
            end,
          },
          jump = { pos = "start" },
        })
      end,
      desc = "Flash word (smart)",
    },
  },
}
