return {
  "sphamba/smear-cursor.nvim",
  event = "VeryLazy",
  opts = {
    -- legacy_computing_symbols_support
    -- Диапазон: true / false
    -- Стандарт: false
    -- За что отвечает: включает использование unicode-символов legacy computing (блоки 1/8, 1/4 и т.д.) для более плавного и аккуратного smear. Без этого диагональные глифы и многие улучшения не работают.
    legacy_computing_symbols_support = true,

    -- use_diagonal_blocks
    -- Диапазон: true / false
    -- Стандарт: true (в новых версиях)
    -- За что отвечает: разрешает использовать специальные диагональные блочные глифы при наклонных движениях курсора → сильно сглаживает углы и убирает «лесенку» на диагоналях. Работает только если legacy_computing_symbols_support = true.
    use_diagonal_blocks = true,

    -- stiffness
    -- Диапазон: 0.0 – 1.0
    -- Стандарт: 0.6
    -- За что отвечает: жёсткость движения головы smear (0 = совсем не двигается, 1 = мгновенно к цели). Выше значение → быстрее реакция, короче общий хвост.
    stiffness = 0.8,

    -- trailing_stiffness
    -- Диапазон: 0.0 – 1.0
    -- Стандарт: 0.45
    -- За что отвечает: жёсткость хвоста (trailing part). Выше → хвост быстрее догоняет голову → визуально хвост короче, менее «тягучий» и более прозрачный.
    trailing_stiffness = 0.6,

    -- stiffness_insert_mode
    -- Диапазон: 0.0 – 1.0
    -- Стандарт: 0.5
    -- За что отвечает: то же, что stiffness, но только в insert-режиме (часто ставят чуть мягче, но мы сделали чуть жёстче для отзывчивости).
    stiffness_insert_mode = 0.7,

    -- trailing_stiffness_insert_mode
    -- Диапазон: 0.0 – 1.0
    -- Стандарт: 0.5
    -- За что отвечает: жёсткость хвоста в insert-режиме. Выше → хвост короче и менее отвлекающий при наборе текста.
    trailing_stiffness_insert_mode = 0.7,

    -- damping
    -- Диапазон: 0.0 – 1.0
    -- Стандарт: 0.85
    -- За что отвечает: коэффициент затухания скорости/колебаний (0 = без затухания, 1 = мгновенная остановка). Выше → анимация быстрее успокаивается, хвост не «прыгает».
    damping = 0.95,

    -- damping_insert_mode
    -- Диапазон: 0.0 – 1.0
    -- Стандарт: 0.9
    -- За что отвечает: затухание в insert-режиме. Выше значение → стабильнее и чище анимация при печати.
    damping_insert_mode = 0.95,

    -- distance_stop_animating
    -- Диапазон: > 0 (обычно 0.1–1.0)
    -- Стандарт: 0.1
    -- За что отвечает: расстояние (в символах), когда хвост достаточно близко к цели и анимация полностью останавливается. Меньше → анимация короче, хвост визуально менее длинный.
    distance_stop_animating = 0.5,

    -- max_angle_difference_diagonal
    -- Диапазон: 0 – π/2 (в радианах), обычно math.pi/20 – math.pi/8
    -- Стандарт: math.pi / 16 ≈ 11.25°
    -- За что отвечает: максимальная разница угла, при которой разрешается использовать диагональный глиф вместо квадрата. Больше значение → чаще диагонали → плавнее диагональные траектории.
    max_angle_difference_diagonal = math.pi / 15, -- ≈ 15°

    -- max_offset_diagonal
    -- Диапазон: 0.0 – 1.0 (в ширинах символа)
    -- Стандарт: 0.2
    -- За что отвечает: максимальное смещение ячейки, при котором ещё пытаемся подогнать диагональный глиф. Больше → лучше работает на длинных диагоналях, меньше артефактов на краях.
    max_offset_diagonal = 0.3,
  },
}
